CREATE VIEW CLASIFICACIONES_VIS
AS SELECT T.ANO, T.COD_TEMPORADA, S.COD_SPLIT , T.COD_EQUIPO, T.POSICION

FROM TEMPORADAS T , SPLITS S, CLASIFICACIONES T

WHERE T.COD_TEMPORADA = S.COD_TEMPORADA
AND
S.COD_SPLIT = T.COD_SPLIT
;

CREATE VIEW PARTICIPANTES_VIS
AS SELECT E.COD_EQUIPO, E.NOMBRE AS NOMBRE_EQUIPO, E.LOGO,
 J.COD_JUGADOR, J.NOMBRE AS NOMBRE_JUGADOR, J.APELLIDO, C.SALARIO,
 J.ALTURA, J.PIE
FROM EQUIPOS E, JUGADORES J, CONTRATOS_EQUIPO_JUGADOR C
WHERE C.COD_EQUIPO = E.COD_EQUIPO
AND C.COD_JUGADOR = J.COD_JUGADOR;




CREATE VIEW Calendario_jornada_VIS AS
SELECT J.FECHA, S.FECHA_INICIO, S.FECHA_FIN
FROM  JORNADAS J , SPLITS S
WHERE J.COD_SPLIT = S.COD_SPLIT
;



CREATE VIEW Resultados_jornadas_VIS AS
SELECT J.COD_SPLIT, J.COD_JORNADA, J.FECHA, P.COD_PARTIDO, I.RESULTADO
FROM JORNADAS J, PARTIDOS P, INFORMACION_PARTIDOS I 
WHERE J.COD_JORNADA = P.COD_JORNADA
AND
P.COD_PARTIDO = I.COD_PARTIDO
;




CREATE SYNONYM TS FOR TEMPORADAS;

CREATE SYNONYM JS FOR JORNADAS;

CREATE SYNONYM PS FOR PARTIDOS;

CREATE SYNONYM ES FOR EQUIPOS;